create table CONTACT(
    contact_id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    contact_type VARCHAR(9) default null,
    contact_info VARCHAR(50) default null,
    person_id BIGINT default null references PERSON(id) on delete cascade,
    PRIMARY KEY(contact_id)
);

create table ROLES(
    role_id BIGINT NOT NULL PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    role_type VARCHAR(50)
);

