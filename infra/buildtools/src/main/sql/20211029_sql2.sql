create table PERSON_ROLE(
    person_id BIGINT,
    role_id BIGINT,
    PRIMARY KEY(person_id, role_id),
    FOREIGN KEY(person_id) REFERENCES PERSON(id) ON DELETE CASCADE,
    FOREIGN KEY(role_id) REFERENCES ROLES(role_id) ON DELETE CASCADE
);